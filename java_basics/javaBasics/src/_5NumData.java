public class _5NumData {
    public static void main(String[] args) {
        
        //Numeros segun extension
        /* byte: Ocupa 1 byte de memoria
        short: Ocupa 2 bytes de memoria
        int: Ocupa 4 bytes de memoria
        long: Ocupa 8 bytes de memoria */

        // Int:
        int n = 1234567890;

        // Long:
        long nL = 123456789012345L; //El long recibe una L al final del numero

        //Numeros flotantes
        /* float: Ocupan 4 bytes de memoria
        double: Ocupan 8 bytes de memoria (maths, mucha precision) */

        // Double:
        double nD = 123.456123456; 

        // Float
        float nF = 123.456F;
    }
}